# Desenvolva um programa que leia as duas notas de um aluno, calcule e mostre a sua média.

print('====== DESAFIO 07 ======')

nome = str (input('Olá, qual seu nome? '))
n1 = float (input(f'Certo {nome}, digite sua primeira nota: '))
n2 = float (input('Agora digite sua segunda nota: '))

print(f'{nome}, a média calculada entre suas notas é: {(n1 + n2) / 2}')


# Faça um algoritmo que leia o preço de um produto e mostre seu novo preço, com 5% de desconto.

print('====== DESAFIO 12 ======')

valorpdt = float (input('Digite o valor do produto para calcularmos seu novo valor com desconto: R$'))
desconto = valorpdt * 0.05

print(f'O novo valor do produto com 5% de desconto é: R${valorpdt - desconto:.2f}')

